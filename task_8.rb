# Напишите функцию, которая экранирует символом '\' следующие символы:
# '_', '*', '[', ']', '(', ')', '~', '`', '>', '#', '+', '-', '=', '|', '{', '}', '.', '!'

def sanitize(txt)
  special_chars = ['_', '*', '[', ']', '(', ')', '~', '`', '>', '#', '+', '-', '=', '|', '{', '}', '.', '!', ',']
  txt.gsub(/([#{special_chars.join('\\')}])/) { |match| "\\#{match}" }
end
